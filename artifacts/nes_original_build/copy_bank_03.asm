sub_80CC_write_to_ppu:
; in
    ; ram_0000_t12_ppu_data
    LDA ram_051D
    ASL
    TAX
    LDA tbl_803C_counter,X
    STA ram_0002_t25_data_counter_hi
    INX
    LDA tbl_803C_counter,X
    STA ram_0003_t05_data_counter_lo
sub_80DC_write_to_ppu:
; in
    ; ram_0002_t25_data_counter_hi
    ; ram_0003_t05_data_counter_lo
    ; ram_0000_t12_ppu_data
    LDA ram_051D
    ASL
    TAX
    LDA tbl_8030_ppu_addr,X
    STA $2006
    INX
    LDA tbl_8030_ppu_addr,X
    STA $2006
    LDY #$00
bra_80F0_loop:
    LDA (ram_0000_t12_ppu_data),Y
    STA $2007
    LDA ram_0000_t12_ppu_data
    CLC
    ADC #< $0001
    STA ram_0000_t12_ppu_data
    LDA ram_0000_t12_ppu_data + $01
    ADC #> $0001
    STA ram_0000_t12_ppu_data + $01
    LDA ram_0003_t05_data_counter_lo
    SEC
    SBC #< $0001
    STA ram_0003_t05_data_counter_lo
    LDA ram_0002_t25_data_counter_hi
    SBC #> $0001
    STA ram_0002_t25_data_counter_hi
; bzk optimize, useless LDA
    LDA ram_0002_t25_data_counter_hi
    BNE bra_80F0_loop
    LDA ram_0003_t05_data_counter_lo
    BNE bra_80F0_loop
    INC ram_051D
    RTS



