sub_0x00C054_fill_ppu_with_tiles_3:
    JSR sub_0x01E635_disable_rendering_and_nmi
    LDA $2002
    JSR sub_805E
    LDA ram_dungeon_level
    BNE bra_8064_entering_dungeon    ; if dungeon
; if overworld
bra_8051_loop:
    JSR sub_8091_select_tiles_and_counter
    JSR sub_80DC_write_to_ppu
    LDA ram_051D
    CMP #$02
    BNE bra_8051_loop
sub_805E:
loc_805E:
    LDA #$00
    STA ram_051D
    RTS
bra_8064_entering_dungeon:
bra_8064_loop:
    JSR sub_8080_select_tiles
    JSR sub_80CC_write_to_ppu
    LDA ram_051D
    CMP #$02
    BNE bra_8064_loop
    JSR sub_80AC_select_tiles
    JSR sub_80CC_write_to_ppu
    JSR sub_80BC_select_tiles
    JSR sub_80CC_write_to_ppu
    JMP loc_805E



sub_8080_select_tiles:
; out
    ; ram_0000_t12_ppu_data
    LDA ram_051D
    ASL
    TAX
    LDA tbl_8028_tiles_dungeon,X
    STA ram_0000_t12_ppu_data
    INX
    LDA tbl_8028_tiles_dungeon,X
    STA ram_0000_t12_ppu_data + $01
    RTS



sub_8091_select_tiles_and_counter:
; out
    ; ram_0000_t12_ppu_data
    ; ram_0002_t25_data_counter_hi
    ; ram_0003_t05_data_counter_lo
    LDA ram_051D
    ASL
    TAX
    LDA tbl_802C_tiles_overworld,X
    STA ram_0000_t12_ppu_data
    LDA tbl_8038_counter_overworld,X
    STA ram_0002_t25_data_counter_hi
    INX
    LDA tbl_802C_tiles_overworld,X
    STA ram_0000_t12_ppu_data + $01
    LDA tbl_8038_counter_overworld,X
    STA ram_0003_t05_data_counter_lo
    RTS



